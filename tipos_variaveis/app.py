# declaração de variáveis
nome  = "João Carlos" # string (texto)
numero = 10 # int (inteiros)
fracao = 3.5 # float (pontos flutuantes/ numeros decimais)
programador = True # boolean (booleanos / lógicos)

# saída de dados
print(nome)
print(numero)
print(fracao)
print(programador)

# para saber qual tipo da variável use o type
print(type(nome))
print(type(numero))
print(type(fracao))
print(type(programador))

