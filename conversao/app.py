# declaração de variáveis
numero = input("Informe um número: ")

#saída de dados
print(f"Valor: {numero}")
print(f"Tipo de dado: {type(numero)}.")

# conversão de tipos
numero = int(numero)

#nova saída de dados
print(f"Valor: {numero}.")
print(f"Novo tipo de dado: {type(numero)}.")

# conversão para float
numero = float(numero)

#nova saída de dados
print(f"Valor: {numero}.")
print(f"Novo tipo de dado: {type(numero)}.")

# conversão para string
numero = str(numero)

#nova saída de dados
print(f"Valor: {numero}.")
print(f"Novo tipo de dado: {type(numero)}.")



